@torch.no_grad()
def evaluate_backbone(model, loader):
    """Return accuracy of the final (backbone) exit head."""
    model.eval()
    correct = 0
    n = 0
    for xb, yb in loader:
        xb, yb = xb.to(DEVICE), yb.to(DEVICE)
        _, _, _, out_final = model(xb)
        correct += (out_final.argmax(-1) == yb).sum().item()
        n += len(yb)
    return correct / n
@torch.no_grad()
def evaluate_early_exit(model, loader, threshold: float):
    """
    Run early-exit inference.
    Returns
    -------
    accuracy       : float
    exit_counts    : dict {1: n, 2: n, 3: n}
    avg_flops_frac : float  (fraction of full-model FLOPs)
    """
    model.eval()
    all_preds, all_labels, all_exits = [], [], []
    for xb, yb in loader:
        xb = xb.to(DEVICE)
        preds, exit_ids = model.forward_with_exit(xb, threshold)
        all_preds.append(preds.cpu())
        all_labels.append(yb)
        all_exits.append(exit_ids.cpu())
    preds = torch.cat(all_preds)
    labels = torch.cat(all_labels)
    exits = torch.cat(all_exits)
    accuracy = (preds == labels).float().mean().item()
    exit_counts = {1: (exits == 1).sum().item(),
                   2: (exits == 2).sum().item(),
                   3: (exits == 3).sum().item()}
    total = len(exits)
    avg_flops = sum(
        (exit_counts[k] / total) * EXIT_FLOPS[k] for k in [1, 2, 3]
    )
    return accuracy, exit_counts, avg_flops
@torch.no_grad()
def evaluate_by_snr(model, dataset: IQDataset, threshold: float, n_bins: int = 7):
    """Evaluate accuracy vs SNR for full model and early-exit."""
    model.eval()
    snr_edges = np.linspace(SNR_MIN, SNR_MAX, n_bins + 1)
    snr_centers = (snr_edges[:-1] + snr_edges[1:]) / 2
    acc_full = np.zeros(n_bins)
    acc_ee = np.zeros(n_bins)
    counts = np.zeros(n_bins, dtype=int)
    X = dataset.X
    y = dataset.y
    snr = torch.tensor(dataset.snr, dtype=torch.float32)
    for i in range(n_bins):
        mask = (snr >= snr_edges[i]) & (snr < snr_edges[i + 1])
        if mask.sum() == 0:
            continue
        xb = X[mask].to(DEVICE)
        yb = y[mask].to(DEVICE)
        _, _, _, out_full = model(xb)
        acc_full[i] = (out_full.argmax(-1) == yb).float().mean().item()
        preds_ee, _ = model.forward_with_exit(xb, threshold)
        acc_ee[i] = (preds_ee == yb).float().mean().item()
        counts[i] = mask.sum().item()
    return snr_centers, acc_full, acc_ee
