def _awgn(signal: np.ndarray, snr_db: float) -> np.ndarray:
    """Add complex AWGN to a complex signal."""
    sig_power = np.mean(np.abs(signal) ** 2)
    snr_linear = 10 ** (snr_db / 10.0)
    noise_power = sig_power / (snr_linear + 1e-12)
    noise = np.sqrt(noise_power / 2) * (
        np.random.randn(*signal.shape) + 1j * np.random.randn(*signal.shape)
    )
    return signal + noise
def _rayleigh_fading(signal: np.ndarray) -> np.ndarray:
    """Apply a flat Rayleigh fading coefficient."""
    h = (np.random.randn() + 1j * np.random.randn()) / math.sqrt(2)
    return h * signal
def _generate_bpsk(n: int, snr_db: float) -> np.ndarray:
    bits = np.random.choice([-1.0, 1.0], size=(n, SEQ_LEN))
    phase_noise = 0.05 * np.random.randn(n, SEQ_LEN)
    symbols = bits * np.exp(1j * phase_noise)
    if snr_db > 10:
        symbols = np.array([_rayleigh_fading(s) for s in symbols])
    return np.array([_awgn(s, snr_db) for s in symbols])
def _generate_qpsk(n: int, snr_db: float) -> np.ndarray:
    indices = np.random.randint(0, 4, size=(n, SEQ_LEN))
    angles = (2 * indices + 1) * np.pi / 4
    symbols = np.exp(1j * angles)
    if snr_db > 10:
        symbols = np.array([_rayleigh_fading(s) for s in symbols])
    return np.array([_awgn(s, snr_db) for s in symbols])
def _generate_8psk(n: int, snr_db: float) -> np.ndarray:
    indices = np.random.randint(0, 8, size=(n, SEQ_LEN))
    angles = 2 * np.pi * indices / 8
    symbols = np.exp(1j * angles)
    if snr_db > 10:
        symbols = np.array([_rayleigh_fading(s) for s in symbols])
    return np.array([_awgn(s, snr_db) for s in symbols])
def _qam_constellation(order: int) -> np.ndarray:
    m = int(math.sqrt(order))
    levels = np.arange(-(m - 1), m, 2, dtype=float)
    re, im = np.meshgrid(levels, levels)
    constellation = (re + 1j * im).flatten()
    constellation /= np.sqrt(np.mean(np.abs(constellation) ** 2))
    return constellation
def _generate_qam(n: int, snr_db: float, order: int) -> np.ndarray:
    const = _qam_constellation(order)
    indices = np.random.randint(0, order, size=(n, SEQ_LEN))
    symbols = const[indices]
    if snr_db > 10:
        symbols = np.array([_rayleigh_fading(s) for s in symbols])
    return np.array([_awgn(s, snr_db) for s in symbols])
def _generate_ofdm(n: int, snr_db: float, n_sub: int = 16) -> np.ndarray:
    results = []
    for _ in range(n):
        subcarriers = (
            np.random.randn(n_sub) + 1j * np.random.randn(n_sub)
        ) / math.sqrt(2)
        # IFFT and tile/truncate to SEQ_LEN
        time_sig = np.fft.ifft(subcarriers, n=SEQ_LEN)
        time_sig /= (np.sqrt(np.mean(np.abs(time_sig) ** 2)) + 1e-12)
        if snr_db > 10:
            time_sig = _rayleigh_fading(time_sig)
        results.append(_awgn(time_sig, snr_db))
    return np.array(results)
def _generate_am(n: int, snr_db: float) -> np.ndarray:
    t = np.linspace(0, 1, SEQ_LEN, endpoint=False)
    results = []
    for _ in range(n):
        fc = np.random.uniform(0.1, 0.4)
        fm = np.random.uniform(0.01, 0.05)
        mod_idx = np.random.uniform(0.3, 0.9)
        carrier = np.cos(2 * np.pi * fc * np.arange(SEQ_LEN))
        message = np.cos(2 * np.pi * fm * np.arange(SEQ_LEN))
        am = (1 + mod_idx * message) * carrier
        # Cast to complex
        sig = am.astype(complex)
        sig /= (np.sqrt(np.mean(np.abs(sig) ** 2)) + 1e-12)
        results.append(_awgn(sig, snr_db))
    return np.array(results)
def _generate_fm(n: int, snr_db: float) -> np.ndarray:
    results = []
    for _ in range(n):
        fc = np.random.uniform(0.1, 0.4)
        kf = np.random.uniform(0.05, 0.2)
        message = np.random.randn(SEQ_LEN)
        phase = 2 * np.pi * fc * np.arange(SEQ_LEN) + 2 * np.pi * kf * np.cumsum(message)
        sig = np.exp(1j * phase)
        results.append(_awgn(sig, snr_db))
    return np.array(results)
def generate_dataset(n_per_class: int = N_SAMPLES_PER_CLASS, seed: int = SEED):
    """Generate synthetic IQ dataset with N_CLASSES modulation types."""
    rng = np.random.RandomState(seed)
    np.random.seed(seed)
    all_iq, all_labels, all_snr = [], [], []
    generators = [
        _generate_bpsk,
        _generate_qpsk,
        _generate_8psk,
        lambda n, s: _generate_qam(n, s, 16),
        lambda n, s: _generate_qam(n, s, 64),
        _generate_ofdm,
        _generate_am,
        _generate_fm,
    ]
    snr_values = np.linspace(SNR_MIN, SNR_MAX, 20)
    for cls_idx, gen in enumerate(generators):
        batch_size = max(1, n_per_class // len(snr_values))
        for snr in snr_values:
            samples = gen(batch_size, snr)          # (B, SEQ_LEN) complex
            iq = np.stack([samples.real, samples.imag], axis=1).astype(np.float32)
            # Normalize per sample
            scale = np.std(iq, axis=(1, 2), keepdims=True) + 1e-8
            iq /= scale
            all_iq.append(iq)
            all_labels.extend([cls_idx] * batch_size)
            all_snr.extend([snr] * batch_size)
    X = np.concatenate(all_iq, axis=0)
    y = np.array(all_labels, dtype=np.int64)
    snr_arr = np.array(all_snr, dtype=np.float32)
    return X, y, snr_arr
