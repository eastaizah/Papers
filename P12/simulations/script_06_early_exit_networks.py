class ExitHead(nn.Module):
    """Lightweight classifier head attached to an intermediate feature map."""
    def __init__(self, in_features: int, n_classes: int):
        super().__init__()
        self.gap = nn.AdaptiveAvgPool1d(1)
        self.fc = nn.Linear(in_features, n_classes)
    def forward(self, x):
        # x: (B, C, L) or (B, C)
        if x.dim() == 3:
            x = self.gap(x).squeeze(-1)
        return self.fc(x)
class MultiExitCNN(nn.Module):
    """
    BranchyNet-style 1D-CNN for modulation recognition.
    Three exit heads are attached at different depths; inference can stop early
    when the softmax confidence exceeds a user-defined threshold τ.
    """
    def __init__(self, n_classes: int = N_CLASSES):
        super().__init__()
        # ---- Backbone ----
        self.layer1 = nn.Sequential(
            nn.Conv1d(2, 32, kernel_size=7, padding=3),
            nn.BatchNorm1d(32),
            nn.ReLU(),
        )
        self.layer2 = nn.Sequential(
            nn.Conv1d(32, 64, kernel_size=5, padding=2),
            nn.BatchNorm1d(64),
            nn.ReLU(),
            nn.MaxPool1d(2),   # 64 → 32
        )
        self.layer3 = nn.Sequential(
            nn.Conv1d(64, 128, kernel_size=3, padding=1),
            nn.BatchNorm1d(128),
            nn.ReLU(),
        )
        self.layer4 = nn.Sequential(
            nn.Conv1d(128, 128, kernel_size=3, padding=1),
            nn.BatchNorm1d(128),
            nn.ReLU(),
            nn.MaxPool1d(2),   # 32 → 16
        )
        self.layer5 = nn.Sequential(
            nn.Conv1d(128, 192, kernel_size=3, padding=1),
            nn.BatchNorm1d(192),
            nn.ReLU(),
        )
        self.layer6 = nn.Sequential(
            nn.Conv1d(192, 192, kernel_size=3, padding=1),
            nn.BatchNorm1d(192),
            nn.ReLU(),
        )
        # ---- Exit heads ----
        self.exit1 = ExitHead(64, n_classes)   # after layer2, GAP → 64
        self.exit2 = ExitHead(128, n_classes)  # after layer4, GAP → 128
        self.exit3 = ExitHead(192, n_classes)  # after layer6, GAP → 192
        # ---- Final classifier (full backbone, additional FC) ----
        self.global_pool = nn.AdaptiveAvgPool1d(1)
        self.final_fc = nn.Sequential(
            nn.Linear(192, 128),
            nn.ReLU(),
            nn.Dropout(0.3),
            nn.Linear(128, n_classes),
        )
    def forward(self, x):
        """Return logits from all exit heads (training mode)."""
        x = self.layer1(x)
        x = self.layer2(x)
        out1 = self.exit1(x)
        x = self.layer3(x)
        x = self.layer4(x)
        out2 = self.exit2(x)
        x = self.layer5(x)
        x = self.layer6(x)
        out3 = self.exit3(x)
        feat = self.global_pool(x).squeeze(-1)
        out_final = self.final_fc(feat)
        return out1, out2, out3, out_final
    def forward_with_exit(self, x, threshold: float):
        """
        Inference with early-exit.
        Returns
        -------
        preds   : (B,) predicted class indices
        exit_id : (B,) exit head used {1, 2, 3}
        """
        B = x.size(0)
        preds = torch.zeros(B, dtype=torch.long, device=x.device)
        exit_ids = torch.zeros(B, dtype=torch.long, device=x.device)
        remaining = torch.ones(B, dtype=torch.bool, device=x.device)
        x = self.layer1(x)
        x = self.layer2(x)
        logits1 = self.exit1(x)
        conf1, pred1 = torch.softmax(logits1, dim=-1).max(dim=-1)
        early = conf1 >= threshold
        mask = remaining & early
        preds[mask] = pred1[mask]
        exit_ids[mask] = 1
        remaining &= ~early
        if remaining.any():
            x = self.layer3(x)
            x = self.layer4(x)
            logits2 = self.exit2(x)
            conf2, pred2 = torch.softmax(logits2, dim=-1).max(dim=-1)
            early2 = conf2 >= threshold
            mask2 = remaining & early2
            preds[mask2] = pred2[mask2]
            exit_ids[mask2] = 2
            remaining &= ~early2
        if remaining.any():
            x = self.layer5(x)
            x = self.layer6(x)
            logits3 = self.exit3(x)
            feat = self.global_pool(x).squeeze(-1)
            out_final = self.final_fc(feat)
            # Use final head for remaining
            _, pred_final = out_final.max(dim=-1)
            preds[remaining] = pred_final[remaining]
            exit_ids[remaining] = 3
        return preds, exit_ids
