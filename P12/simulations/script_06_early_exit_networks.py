def train_epoch(model, loader, optimizer, criterion, weights):
    model.train()
    total_loss = 0.0
    correct_final = 0
    n = 0
    for xb, yb in loader:
        xb, yb = xb.to(DEVICE), yb.to(DEVICE)
        optimizer.zero_grad()
        out1, out2, out3, out_final = model(xb)
        loss = (
            weights[0] * criterion(out1, yb)
            + weights[1] * criterion(out2, yb)
            + weights[2] * criterion(out_final, yb)
        )
        loss.backward()
        nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        optimizer.step()
        total_loss += loss.item() * len(yb)
        correct_final += (out_final.argmax(-1) == yb).sum().item()
        n += len(yb)
    return total_loss / n, correct_final / n
@torch.no_grad()
def eval_epoch(model, loader, criterion, weights):
    model.eval()
    total_loss = 0.0
    correct = [0, 0, 0]
    n = 0
    for xb, yb in loader:
        xb, yb = xb.to(DEVICE), yb.to(DEVICE)
        out1, out2, out3, out_final = model(xb)
        loss = (
            weights[0] * criterion(out1, yb)
            + weights[1] * criterion(out2, yb)
            + weights[2] * criterion(out_final, yb)
        )
        total_loss += loss.item() * len(yb)
        correct[0] += (out1.argmax(-1) == yb).sum().item()
        correct[1] += (out2.argmax(-1) == yb).sum().item()
        correct[2] += (out_final.argmax(-1) == yb).sum().item()
        n += len(yb)
    return total_loss / n, [c / n for c in correct]
def train_model(model, train_loader, val_loader, n_epochs=30, patience=5):
    criterion = nn.CrossEntropyLoss()
    weights = [0.25, 0.35, 0.40]
    optimizer = optim.Adam(model.parameters(), lr=2e-3, weight_decay=1e-4)
    scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=n_epochs, eta_min=1e-5)
    history = {
        "train_loss": [], "val_loss": [],
        "train_acc_final": [],
        "val_acc_e1": [], "val_acc_e2": [], "val_acc_final": [],
    }
    best_val_loss = math.inf
    best_state = None
    no_improve = 0
    for epoch in range(1, n_epochs + 1):
        t0 = time.time()
        tr_loss, tr_acc = train_epoch(model, train_loader, optimizer, criterion, weights)
        vl_loss, vl_accs = eval_epoch(model, val_loader, criterion, weights)
        scheduler.step()
        history["train_loss"].append(tr_loss)
        history["val_loss"].append(vl_loss)
        history["train_acc_final"].append(tr_acc)
        history["val_acc_e1"].append(vl_accs[0])
        history["val_acc_e2"].append(vl_accs[1])
        history["val_acc_final"].append(vl_accs[2])
        elapsed = time.time() - t0
        print(
            f"Epoch {epoch:3d}/{n_epochs} | "
            f"Loss {tr_loss:.4f}/{vl_loss:.4f} | "
            f"Acc E1={vl_accs[0]:.3f} E2={vl_accs[1]:.3f} Final={vl_accs[2]:.3f} | "
            f"{elapsed:.1f}s"
        )
        if vl_loss < best_val_loss:
            best_val_loss = vl_loss
            best_state = {k: v.clone() for k, v in model.state_dict().items()}
            no_improve = 0
        else:
            no_improve += 1
            if no_improve >= patience:
                print(f"Early stopping at epoch {epoch}.")
                break
    model.load_state_dict(best_state)
    return history
