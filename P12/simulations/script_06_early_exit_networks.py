def main():
    print("=" * 70)
    print("Script 06 – Early-Exit Networks for 6G AMR")
    print("=" * 70)
    # ------------------------------------------------------------------
    # 1. Dataset
    # ------------------------------------------------------------------
    print("\n[1/4] Generating synthetic IQ dataset …")
    X, y, snr = generate_dataset(n_per_class=N_SAMPLES_PER_CLASS)
    print(f"      Total samples: {len(y):,}  |  Shape: {X.shape}")
    # Train / Val / Test split
    n = len(y)
    idx = np.random.permutation(n)
    n_train = int(0.70 * n)
    n_val   = int(0.15 * n)
    train_idx = idx[:n_train]
    val_idx   = idx[n_train:n_train + n_val]
    test_idx  = idx[n_train + n_val:]
    ds_train = IQDataset(X[train_idx], y[train_idx], snr[train_idx])
    ds_val   = IQDataset(X[val_idx],   y[val_idx],   snr[val_idx])
    ds_test  = IQDataset(X[test_idx],  y[test_idx],  snr[test_idx])
    train_loader = DataLoader(ds_train, batch_size=256, shuffle=True,  num_workers=0)
    val_loader   = DataLoader(ds_val,   batch_size=512, shuffle=False, num_workers=0)
    test_loader  = DataLoader(ds_test,  batch_size=512, shuffle=False, num_workers=0)
    print(f"      Train: {len(ds_train):,}  Val: {len(ds_val):,}  Test: {len(ds_test):,}")
    # ------------------------------------------------------------------
    # 2. Train
    # ------------------------------------------------------------------
    print("\n[2/4] Training multi-exit CNN …")
    model = MultiExitCNN(n_classes=N_CLASSES).to(DEVICE)
    n_params = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"      Parameters: {n_params:,}")
    history = train_model(model, train_loader, val_loader, n_epochs=30, patience=8)
    # ------------------------------------------------------------------
    # 3. Plot training
    # ------------------------------------------------------------------
    print("\n[3/4] Saving figures …")
    plot_training(history)
    # ------------------------------------------------------------------
    # 4. Evaluate
    # ------------------------------------------------------------------
    backbone_acc = evaluate_backbone(model, test_loader)
    print(f"\n      Backbone (full model) accuracy: {backbone_acc:.4f}")
    thresholds = [0.5, 0.7, 0.8, 0.9, 0.95, 0.99]
    results = {}
    for tau in thresholds:
        acc, ec, avg_flops = evaluate_early_exit(model, test_loader, tau)
        results[tau] = {"accuracy": acc, "exit_counts": ec, "avg_flops": avg_flops}
        lr = 1.0 / avg_flops
        print(
            f"  τ={tau:.2f} | acc={acc:.4f} ({acc/backbone_acc:.3f}× backbone) "
            f"| avg FLOPs={avg_flops:.3f} ({lr:.2f}× reduction) "
            f"| exits: {ec}"
        )
    plot_exit_distribution(results)
    plot_accuracy_latency(results, backbone_acc)
    snr_centers, acc_full_snr, acc_ee_snr = evaluate_by_snr(model, ds_test, threshold=0.8)
    plot_accuracy_vs_snr(snr_centers, acc_full_snr, acc_ee_snr)
    # ------------------------------------------------------------------
    # 5. Verification
    # ------------------------------------------------------------------
    print("\n" + "=" * 70)
    print("VERIFICATION SUMMARY")
    print("=" * 70)
    checks = {}
    # Check 1: backbone accuracy ≥ 85 %
    checks["backbone_acc"] = backbone_acc >= 0.85
    status = "PASS ✓" if checks["backbone_acc"] else "FAIL ✗"
    print(f"[{status}] Backbone accuracy ≥ 85% : {backbone_acc:.4f}")
    # Check 2: τ=0.7 avg FLOPs < 50 % (≥ 2× reduction)
    avg_flops_07 = results[0.7]["avg_flops"]
    checks["latency_2x"] = avg_flops_07 < 0.50
    status = "PASS ✓" if checks["latency_2x"] else "FAIL ✗"
    print(
        f"[{status}] τ=0.7 avg latency reduction ≥ 2× : "
        f"{1/avg_flops_07:.2f}× (avg FLOPs={avg_flops_07:.3f})"
    )
    # Check 3: τ=0.7 accuracy ≥ 90 % of backbone
    acc_07 = results[0.7]["accuracy"]
    rel_acc_07 = acc_07 / backbone_acc
    checks["acc_retained_07"] = rel_acc_07 >= 0.90
    status = "PASS ✓" if checks["acc_retained_07"] else "FAIL ✗"
    print(
        f"[{status}] τ=0.7 accuracy ≥ 90% of backbone : "
        f"{rel_acc_07:.3f} ({acc_07:.4f} / {backbone_acc:.4f})"
    )
    # Check 4: τ=0.5 avg FLOPs < 33 % (≥ 3× reduction)
    avg_flops_05 = results[0.5]["avg_flops"]
    checks["latency_3x"] = avg_flops_05 < 1 / 3.0
    status = "PASS ✓" if checks["latency_3x"] else "FAIL ✗"
    print(
        f"[{status}] τ=0.5 avg latency reduction ≥ 3× : "
        f"{1/avg_flops_05:.2f}× (avg FLOPs={avg_flops_05:.3f})"
    )
    n_pass = sum(checks.values())
    n_total = len(checks)
    print(f"\nOverall: {n_pass}/{n_total} checks passed.")
    print("=" * 70)
